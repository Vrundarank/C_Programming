void DisplayASCII()
{   
    int i = 0;
    printf("--------------------------------------------------------\n");
    printf("|  DEC  |  HEX  |  OCT  |  CHAR  |\n");
    printf("--------------------------------------------------------\n");

    for (i = 0; i <= 255; i++)
    {
        char ch = 0;

        //Shows printable characters, else show dot
        if ((i >= 32) && (i <= 126))
        {
            ch = i;
        }
        else
        {
            ch = '.';
        }
    printf("| %4d |  %02X  | %03o  |   %c    |\n", i, i, i, ch);
    }
    printf("--------------------------------------------------------\n");
    
}

////////////////////////////////////////////////////////////////////////
//
//  Entry point function : Main
//
////////////////////////////////////////////////////////////////////////

int main()
{

    DisplayASCII();

    return 0;
}
